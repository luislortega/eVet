    public void buscarCitas(String user){
        DefaultTableModel m;
        try {
            String titulo[] = {
                "No. de cita",
                "De",
                "Num Tel",
                "mensaje"
            };
            m = new DefaultTableModel(null, titulo);
            JTable p = new JTable(m);
            String fila[] = new String[5];
            Conexion cdb = ConectarServicio.getInstancia().getConexionDb();
            cdb.un_sql = "SELECT * FROM citas WHERE para='"+user+"';";
            cdb.resultado = cdb.us_st.executeQuery(cdb.un_sql);
            int index = 1;
            
            while (cdb.resultado.next()) {                
                fila[0] = String.valueOf(index);
                fila[1] = cdb.resultado.getString(3);
                fila[2] = cdb.resultado.getString(4);
                fila[3] = cdb.resultado.getString(5);
                m.addRow(fila);
                index++;
            }
            tblCitas.setModel(m);
            TableRowSorter<TableModel> ordenar = new TableRowSorter<TableModel>(m);
            tblCitas.setRowSorter(ordenar);
            this.tblCitas.setModel(m);
            this.tblCitas.setRowHeight(66);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al extraer los datos a la tabla", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
