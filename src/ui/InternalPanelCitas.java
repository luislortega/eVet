    private void tblCitasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCitasMousePressed
        int cita = this.tblCitas.getSelectedRow();
        try {          
            this.lblDe.setText(tblCitas.getValueAt(cita, 1).toString());
            this.lblNumCel.setText(tblCitas.getValueAt(cita, 2).toString());
            this.taMensaje.setText(tblCitas.getValueAt(cita, 3).toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cargar datos de el proveedor", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tblCitasMousePressed

    private void btnBorrarCitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarCitaActionPerformed
        try {
            if (!lblDe.getText().isEmpty()) {
                ControladorCitas cc = new ControladorCitas();
                cc.BorrarCita(lblDe.getText(), taMensaje.getText());
            }else{
                JOptionPane.showMessageDialog(null, "Lo siento, no has seleccionado una cita para borrarla.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: "+e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBorrarCitaActionPerformed

